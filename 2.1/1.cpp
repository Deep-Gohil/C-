#include<iostream>
using namespace std;

int main()
{
	int a=12,b=8;
	
	cout << "Addition Of " << a << " And " << b << " Is : " << a+b << endl;
	cout << "Substraction Of " << a << " And " << b << " Is : " << a-b << endl;
	cout << "Multiplication Of " << a << " And " << b << " Is : " << a*b << endl;
	cout << "Division Of " << a << " And " << b << " Is : " << a/b << endl;
	cout << "Modulas Of " << a << " And " << b << " Is : " << a%b << endl; 
	
	return 0;
}
