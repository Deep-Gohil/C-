#include<iostream>
using namespace std;

int main()
{
	int a,b;
	cout << "Enter A Number ";
	cin >> a;
	cout << "Enter B Number ";
	cin >> b;
	
	cout << "Addition Is : " << a+b << endl;
	cout << "Substraction Is : " << a-b << endl;
	cout << "Multiplication Is : " << a*b << endl;
	cout << "Division Is : " << a/b << endl;
	cout << "Modulas Is : " << a%b << endl;
	return 0;
}
